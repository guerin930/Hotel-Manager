    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            int ligne = list.getSelectedRow();
            String cible = list.getModel().getValueAt(ligne, 6).toString();
        try{
            if(JOptionPane.showConfirmDialog(null, "vous allez supprimer une reservation , voulez vous continuer ?","reservation supprimer avec success",JOptionPane.YES_NO_OPTION)== JOptionPane.OK_OPTION)
            if( cible.length() != 0){
                st.executeUpdate("delete from reservation where idReservation="+cible);
            }else{JOptionPane.showMessageDialog(null, "veuillez remplir la date de reservation svp!");}
        }catch(HeadlessException | SQLException e){JOptionPane.showMessageDialog(null, "erreur de suppression "+e.getMessage());}

        actualiser();
    }//GEN-LAST:event_jButton2ActionPerformed

